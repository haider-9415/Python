# we can convert integer or a string that contains decimal no.

# integer --> float
int1 = 1234
int2 = 987692
print("integer --> float")
print("    ",int1,"-->",float(int1))
print("    ",int2,"-->",float(int2))


# string --> float
str1 = '3213' # decimal 
str2 = '1103' # decimal
print("string --> float")
print("    ",str1,"-->",float(str1))
print("    ",str2,"-->",float(str2))

