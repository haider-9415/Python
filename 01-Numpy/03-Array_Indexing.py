import numpy as np

""" for 1D array """
arr1 = np.array(['a','b',1,2,3,100,None])

print('arr1 --> ',arr1)
print('\n.............................................\n')

# from starting to ending
print('arr1[0] --> ',arr1[0])
print('arr1[1] --> ',arr1[1])
print('arr1[2] --> ',arr1[2])
print('arr1[3] --> ',arr1[3])
print('arr1[4] --> ',arr1[4])
print('arr1[5] --> ',arr1[5])
print('arr1[6] --> ',arr1[6])
print('\n.............................................\n')

# from ending to starting
print('arr1[-1] --> ',arr1[-1])
print('arr1[-2] --> ',arr1[-2])
print('arr1[-3] --> ',arr1[-3])
print('arr1[-4] --> ',arr1[-4])
print('arr1[-5] --> ',arr1[-5])
print('arr1[-6] --> ',arr1[-6])
print('arr1[-7] --> ',arr1[-7])
print('\n.............................................\n')



