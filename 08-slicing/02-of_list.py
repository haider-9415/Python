# as we do slicing in string similarly we can do slicing in any list
l1 = [1,2,10,34.6,5,'haider',45+1j,3-823j]
print("l1[0:8] -->",l1[0:8])
print("l1[0:7] -->",l1[0:7])
print("l1[0:5] -->",l1[0:5])
print("l1[3:8] -->",l1[3:8])
print("l1[5:8] -->",l1[5:8])

print("l1[0:8:2] -->",l1[0:8:2])
print("l1[4:8:3] -->",l1[4:8:3])
print("l1[0:8:-1] -->",l1[0:8:-1])
print("l1[::-1] -->",l1[::-1])
print("l1[::-2] -->",l1[::-2])
