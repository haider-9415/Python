""" to insert an element in a list --> .insert(x,y),where, 'x' tells that where you want to insert it and 'y' 
    tells that what you want to insert
"""


l1 = [43,5,4,6.5,6,5.6,433.67,0,-445,-344,1,-1]
l2 = ['abc','wed','aaf','fd']

l2.insert(2,900)
print('l2 --> ',l2,'\n') # now '900' is in l2 at 2nd index

l1.insert(4,'haider')
print('l1 --> ',l1,'\n') # now 'haider' is in l1 at 2nd index