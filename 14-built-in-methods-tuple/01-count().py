""" to find no. of occurrences of an element in tuple --> .count(element) """

tp1 = (1,2,3,2,'haider','hanzala',9-45j,'haider')

print("no. of occurrences of 2 in tp1 = ",tp1.count(2),'\n')
print("no. of occurrences of 5 in tp1 = ",tp1.count(5),'\n')
print("no. of occurrences of 'haider' in tp1 = ",tp1.count('haider'),'\n')
